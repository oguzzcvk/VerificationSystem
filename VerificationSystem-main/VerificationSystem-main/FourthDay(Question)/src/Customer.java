public class Customer {

    int id;
    String firstName;
    String lastName;
    double birthOfYear;
    String nationalId;

    public Customer(int id, String firstName, String lastName, double birthOfYear, String nationalId) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.birthOfYear = birthOfYear;
        this.nationalId = nationalId;
    }
}
